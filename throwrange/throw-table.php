function throwMoves($x) {
	switch($x) {
		case 'balrog':
			return array('hp-throw','mp-throw');
		case 'ehonda':
			return array('throw','oicho');
		case 'thawk':
			return array('throw','360 | super');
		case 'zangief':
			//return array('super','lk suplex','throw | mk suplex','hk suplex','spd','lk running bear grab','mk running bear grab','hk running bear grab');
			return array('super','lk suplex','throw | mk suplex','hk suplex','spd');
		default:
			return array('throw');
	}
}

