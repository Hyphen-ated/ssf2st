foreach($characters as $x) {
	$charMoves = throwMoves($x);
	foreach($charMoves as $charMove) {
		$moves[] = $x . "/" . $charMove;
	}
}

$moveScores = array();

foreach($moves as $move) {
	$moveScores[$move] = 0;
	
	list($self, $action) = explode('/', $move);
	
	foreach($characters as $opponent) {
		$opponentActions = throwMoves($opponent);
		foreach($opponentActions as $opponentAction) {
			$myRange = $table[$self][$opponent][$action];
			$opponentRange = $table[$opponent][$self][$opponentAction];
			if ($myRange > $opponentRange) {
				$moveScores[$move] += 1;
			} elseif ($myRange < $opponentRange) {
				$moveScores[$move] -= 1;
			}
		}
	}
}

